const express = require("express");
const router = express.Router();
const bodyParser = require("body-parser");
var urlencodedParser = bodyParser.urlencoded({ extended: false });
const TeamLeadersController = require("../../controllers/menu/TeamLeadersController.js");

//Middle ware that is specific to this router
router.use(function timeLog(req, res, next) {
  next();
});

router.post("/add_team_leaders", urlencodedParser, function(
  request,
  response
) {
  var jsonObject_ = {
    TeamId: request.body.TeamId,
    AdministratorId: request.body.AdministratorId
  };

  var myPromise = TeamLeadersController.insert(jsonObject_);

  myPromise.then(
    function(result) {
      var response_object = { results: result };
      response.send(response_object);
    },
    function(err) {
      console.log(err);
      response.send("An error occurred");
    }
  );
});

